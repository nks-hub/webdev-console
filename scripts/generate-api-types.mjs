#!/usr/bin/env node
/**
 * Fetches the OpenAPI spec from a running daemon and generates TypeScript
 * types into src/frontend/src/api/generated-types.ts.
 *
 * Usage:
 *   node scripts/generate-api-types.mjs [--port=5146] [--token=...]
 *
 * Intended to run in CI so contract drift between C# daemon and Vue frontend
 * is detected via type errors before merging. Locally you can run it after
 * adding new endpoints to the daemon.
 *
 * Requires: dev daemon running OR port file at %TEMP%/nks-wdc-daemon.port
 */

import { readFileSync, existsSync, writeFileSync, mkdirSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { tmpdir } from 'node:os'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = join(__dirname, '..')
const OUT_FILE = join(REPO_ROOT, 'src', 'frontend', 'src', 'api', 'generated-types.ts')

function readPortFile() {
  const p = join(tmpdir(), 'nks-wdc-daemon.port')
  if (!existsSync(p)) return null
  const lines = readFileSync(p, 'utf-8').split('\n').filter(Boolean)
  if (lines.length < 2) return null
  return { port: parseInt(lines[0], 10), token: lines[1] }
}

const args = process.argv.slice(2)
const argPort = args.find(a => a.startsWith('--port='))?.split('=')[1]
const argToken = args.find(a => a.startsWith('--token='))?.split('=')[1]

const info = readPortFile()
const port = argPort || info?.port || 5146
const token = argToken || info?.token || ''

const url = `http://localhost:${port}/openapi/v1.json`
console.log(`[generate-api-types] Fetching ${url}`)

try {
  const res = await fetch(url, {
    headers: token ? { Authorization: `Bearer ${token}` } : {},
  })
  if (!res.ok) {
    console.error(`[generate-api-types] HTTP ${res.status} ${res.statusText}`)
    process.exit(1)
  }
  const spec = await res.json()
  console.log(`[generate-api-types] ${Object.keys(spec.paths || {}).length} paths`)

  // Simple type extraction from paths — enough to catch drift.
  // For production-grade types use swagger-typescript-api (npm) in CI.
  const paths = Object.keys(spec.paths || {}).sort()
  const banner = `/**
 * AUTO-GENERATED — do not edit manually.
 * Generated by scripts/generate-api-types.mjs from daemon OpenAPI spec.
 * To regenerate: node scripts/generate-api-types.mjs
 */
`
  const pathsConst = `export const API_PATHS = ${JSON.stringify(paths, null, 2)} as const

export type ApiPath = (typeof API_PATHS)[number]
`

  mkdirSync(dirname(OUT_FILE), { recursive: true })
  writeFileSync(OUT_FILE, banner + '\n' + pathsConst, 'utf-8')
  console.log(`[generate-api-types] Wrote ${OUT_FILE}`)
} catch (err) {
  console.error(`[generate-api-types] Failed:`, err.message)
  console.error(`[generate-api-types] Is the daemon running? Start it with: dotnet run --project src/daemon/NKS.WebDevConsole.Daemon`)
  process.exit(1)
}
